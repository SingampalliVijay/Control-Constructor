package com.kn.dowhile;

public class MatrixPattern {

	public static void main(String[] args) {
		System.out.println("Printing a Pattern");
		for(int i=1;i<=9.;i++) {
			for(int j=1;j<=10;j++) {
				System.out.print(j); // where j is column & i is row
			}System.out.println();
		}
	}
}
